<?php

namespace App\Observers;

use App\Models\User;

class UserObserver
{
    /**
     * Handle the User "created" event.
     */
    public function created(User $user): void
    {
        //Asociar este user con el rol segun el tipo de usuario
        switch ($user->tipo) {
            case User::ADMIN:
                $user->assignRole('super_admin');

                break;
            case User::OFICINAS:
                $user->assignRole('oficina');

                break;
            case User::DOCENTES:
                $user->assignRole('profesor');

                break;
        }
    }

